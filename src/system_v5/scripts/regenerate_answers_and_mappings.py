print("\nRegenerating answers and mappings...")
updated_count = 0
error_count = 0

for idx, item in enumerate(dataset):
    item_id = item.get("id", idx)
    
    if idx % 10 == 0:
        print(f"Processing item {idx+1}/100...")
    
    try:
        # Skip items marked as absent (not answerable)
        ontology_presence = item.get("ontology_presence", "")
        
        if ontology_presence == "absent":
            # For absent items, keep "I can't answer the question"
            item["gold_answer_agent"] = {
                "reasoning": "No supporting ontology facts were found for the entity.",
                "answer": "I can't answer the question."
            }
            item["gold_mapped_answer"] = {
                "entity_side": {
                    "types": [],
                    "superclasses": [],
                    "equivalent_classes": [],
                    "properties": [],
                    "property_values": {}
                }
            }
            continue
        
        # Get entity name from flat structure
        entity_dict = item.get("entity", {})
        entity_name = entity_dict.get("value", "") if isinstance(entity_dict, dict) else entity_dict
        
        if not entity_name:
            print(f"  {item_id}: No entity found")
            error_count += 1
            continue
        
        # Retrieve entity context
        context = get_entity_context(entity_name, graph)
        
        if not context:
            print(f"  {item_id}: Could not find context for {entity_name}")
            error_count += 1
            continue
        
        # Generate evidence-based answer
        gold_answer = formulate_answer_with_evidence(item, context)
        
        # Simulate map_answer_to_context
        mapped = pipeline_map_answer_to_context(gold_answer, context)
        
        # Update item
        item["gold_answer_agent"] = {
            "reasoning": f"The ontology evidence supports the answer: {gold_answer}",
            "answer": gold_answer
        }
        item["gold_mapped_answer"] = {
            "entity_side": mapped
        }
        
        updated_count += 1
        
    except Exception as e:
        print(f"  {item_id}: Error - {str(e)}")
        error_count += 1
        continue

print(f"\nCompleted: {updated_count} items updated, {error_count} errors")

